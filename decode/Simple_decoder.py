# Updated Sequence Mapping
sequence_mapping = [
    ["_AALScout"], ["_BEAT"], ["_CV"], ["_MRF"], ["_blade"], ["_bs"], ["_ciss"], ["_csi"], ["_dess"], ["_ep"],
    ["_ep2d"], ["_fast"], ["_fid"], ["_fl"], ["_fl3d"], ["_gre"], ["_greMRE"], ["_haste"], ["_medic"], ["_petra"],
    ["_psif"], ["_qDWI"], ["_resolve"], ["_se"], ["_semac"], ["_space"], ["_svs"], ["_tfl"], ["_tgse"], ["_trufi"],
    ["_tse"], ["_twist"]
]


# Decode the data
decoded_data = [sequence_mapping[i][0] for i, sequence in enumerate(encoded_data) if 1 in sequence]

# Output to CSV with quotechar to handle special characters
output_csv = "model_results/body12_combined.csv"
with open(output_csv, mode='w', newline='') as file:
    writer = csv.writer(file, quotechar='"', quoting=csv.QUOTE_MINIMAL)
    writer.writerow(["Sequence"])
    for sequence in decoded_data:
        writer.writerow([sequence])

print(f"Decoded data has been saved to {output_csv}")

